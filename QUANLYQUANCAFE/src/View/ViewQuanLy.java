    private void fillData(int index) {
        QuanLy ql = list.get(index);
        txtMa.setText(ql.getMaQL());
        txtSDT.setText(ql.getSDT());
        txtTen.setText(ql.getTenQL());
        txtDiaChi.setText(ql.getDiaChi());
    }
    
    private void showData(List<QuanLy> list) {
        dtm.setRowCount(0);
        for (QuanLy ql : list) {
            dtm.addRow(new Object[]{ql.getMaQL(), ql.getTenQL(), ql.getSDT(), ql.getDiaChi()});
        }
    }
    private void tbQuanLyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbQuanLyMouseClicked
        // TODO add your handling code here:
        int row = tbQuanLy.getSelectedRow();
        fillData(row);
    }//GEN-LAST:event_tbQuanLyMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String sdt = txtSDT.getText();
        String diaChi = txtDiaChi.getText();
        QuanLyModel ql = new QuanLyModel("", ma, ten, sdt, diaChi);
        JOptionPane.showMessageDialog(this, qlService.add(ql));
        list = qlService.getAll();
        showData(list);
    }//GEN-LAST:event_btnThemActionPerformed

