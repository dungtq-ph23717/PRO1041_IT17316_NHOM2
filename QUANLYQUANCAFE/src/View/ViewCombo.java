    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn Xóa?");
        var temp = new ComboModel();
        if (option == 0) {
            temp.setId(_idWhenClick);
            JOptionPane.showMessageDialog(this, _ComboService.delete(temp));
            LoadTable();
            _idWhenClick = "";
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        var temp = GetDataFromGui();
        temp.setId(_idWhenClick);
        JOptionPane.showMessageDialog(this, _ComboService.update(temp));
        LoadTable();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void tbl_comboMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_comboMouseClicked
        // TODO add your handling code here:
        int rowIndex = tbl_combo.getSelectedRow();
        if (rowIndex == -1) {
            return;
        }
        _idWhenClick = tbl_combo.getModel().getValueAt(rowIndex, 1).toString();
        var temp = _ComboService.getById(_idWhenClick);
        txt_ma.setText(temp.getMaCB());
        txt_ten.setText(temp.getTenCB());
        txt_giaban.setText(String.valueOf(temp.getGiaBan()));
    }//GEN-LAST:event_tbl_comboMouseClicked

